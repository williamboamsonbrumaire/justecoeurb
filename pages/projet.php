<script>
document.addEventListener('DOMContentLoaded', () => {
    const columns = document.querySelectorAll('.column');
    const speed = 0.5; // Vitesse de défilement (pixels par frame)

    // 1. Dupliquer les images pour l'effet de boucle
    columns.forEach(column => {
        const images = column.innerHTML;
        // Dupliquer le contenu pour garantir le défilement continu
        column.innerHTML += images; 
    });

    // 2. Fonction d'Animation
    function animate() {
        columns.forEach(column => {
            const direction = column.dataset.direction === 'up' ? -1 : 1;
            let currentTransform = getComputedTranslateY(column);
            
            // Calculer la nouvelle position
            let newTransform = currentTransform + (direction * speed);
            
            const columnHeight = column.offsetHeight / 2; // Hauteur initiale (avant duplication)

            // Logique de bouclage pour l'effet infini
            if (direction === -1 && newTransform < -columnHeight) {
                // Si défilement vers le haut et dépasse la moitié (le point de duplication)
                newTransform += columnHeight;
            } else if (direction === 1 && newTransform > 0) {
                // Si défilement vers le bas et dépasse le point de départ
                newTransform -= columnHeight;
            }

            // Appliquer la transformation
            column.style.transform = `translateY(${newTransform}px)`;
        });

        requestAnimationFrame(animate); // Appeler la fonction sur la prochaine frame
    }

    // Fonction utilitaire pour lire le translateY actuel
    function getComputedTranslateY(obj) {
        const style = window.getComputedStyle(obj);
        const matrix = style.transform || style.webkitTransform || style.mozTransform;

        if (matrix.includes('3d')) {
            return parseFloat(matrix.split(', ')[13]);
        } else if (matrix.includes('matrix')) {
            return parseFloat(matrix.split(', ')[5]);
        }
        return 0; // Retourne 0 si aucune transformation n'est appliquée
    }

    // Démarrer l'animation
    animate();
});
</script>

<script>
    const buttons = document.querySelectorAll(".filters button");

buttons.forEach(btn => {
  btn.addEventListener("click", () => {
    buttons.forEach(b => b.classList.remove("active"));
    btn.classList.add("active");
  });
});

document.addEventListener('DOMContentLoaded', () => {
    // 1. Sélectionner les éléments clés
    const filterButtons = document.querySelectorAll('.filters button');
    const projectCards = document.querySelectorAll('.project-grid .project-card');

    // 2. Fonction principale de filtrage
    const filterProjects = (category) => {
        
        // Parcourir toutes les cartes de projet
        projectCards.forEach(card => {
            const cardCategories = card.getAttribute('data-category');

            // 1. Cas "Tous les projets"
            if (category === 'all') {
                card.style.display = 'block'; // Afficher toutes les cartes
                return;
            }

            // 2. Cas des filtres spécifiques
            // On vérifie si la catégorie cliquée est présente dans la liste des catégories de la carte
            if (cardCategories && cardCategories.includes(category)) {
                card.style.display = 'block'; // Afficher la carte correspondante
            } else {
                card.style.display = 'none'; // Masquer les autres
            }
        });
    }

    // 3. Attacher l'événement de clic à chaque bouton de filtre
    filterButtons.forEach(button => {
        button.addEventListener('click', (e) => {
            // Désactiver la classe 'active' sur tous les boutons
            filterButtons.forEach(btn => btn.classList.remove('active'));

            // Activer la classe 'active' sur le bouton cliqué
            e.target.classList.add('active');

            // Récupérer la valeur du filtre (e.g., "entrepreneuriat")
            const filterValue = e.target.getAttribute('data-filter');

            // Appeler la fonction de filtrage
            filterProjects(filterValue);
        });
    });

    // 4. Exécuter le filtre "all" au chargement pour s'assurer de l'état initial
    filterProjects('all'); 
});

</script>

