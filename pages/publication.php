const articles = Array.from(document.querySelectorAll('#articles-data li')).map(li => ({
  img:   li.dataset.img,
  title: li.dataset.title,
  url:   li.dataset.url
}));

const videos = Array.from(document.querySelectorAll('#videos-data li')).map(li => ({
  title: li.dataset.title,
  embed: li.dataset.embed
}));

/* ══════════════════════════════
   MOTEUR DE PAGINATION GÉNÉRIQUE
══════════════════════════════ */
function Paginator({ data, perPage, gridId, paginationId, infoId, renderItem, gridClass }) {
  let currentPage = 1;
  const totalPages = () => Math.ceil(data.length / perPage);

  function render() {
    // Grille
    const grid = document.getElementById(gridId);
    grid.className = gridClass || 'grid-layout';
    const start = (currentPage - 1) * perPage;
    const slice = data.slice(start, start + perPage);
    grid.innerHTML = slice.map(renderItem).join('');
    // Force animation replay
    grid.style.animation = 'none';
    grid.offsetHeight; // reflow
    grid.style.animation = '';

    // Info
    const info = document.getElementById(infoId);
    const end = Math.min(start + perPage, data.length);
    info.textContent = `Affichage ${start + 1}–${end} sur ${data.length} éléments`;

    // Pagination
    const pag = document.getElementById(paginationId);
    pag.innerHTML = '';

    const tp = totalPages();
    if (tp <= 1) return;

    // Bouton précédent
    const prev = btn('<i class="fas fa-chevron-left"></i>', currentPage === 1);
    prev.classList.add('arrow');
    prev.onclick = () => { if (currentPage > 1) { currentPage--; render(); } };
    pag.appendChild(prev);

    // Pages
    const pages = getPages(currentPage, tp);
    pages.forEach(p => {
      if (p === '…') {
        const el = document.createElement('span');
        el.textContent = '…';
        el.style.cssText = 'padding:0 4px;color:#aaa;line-height:40px;';
        pag.appendChild(el);
      } else {
        const b = btn(p, false);
        if (p === currentPage) b.classList.add('active');
        b.onclick = () => { currentPage = p; render(); };
        pag.appendChild(b);
      }
    });

    // Bouton suivant
    const next = btn('<i class="fas fa-chevron-right"></i>', currentPage === tp);
    next.classList.add('arrow');
    next.onclick = () => { if (currentPage < tp) { currentPage++; render(); } };
    pag.appendChild(next);
  }

  function btn(html, disabled) {
    const b = document.createElement('button');
    b.innerHTML = html;
    b.className = 'page-btn';
    b.disabled = disabled;
    return b;
  }

  function getPages(cur, total) {
    if (total <= 7) return Array.from({length: total}, (_, i) => i + 1);
    const pages = [];
    if (cur <= 4) {
      pages.push(1, 2, 3, 4, 5, '…', total);
    } else if (cur >= total - 3) {
      pages.push(1, '…', total-4, total-3, total-2, total-1, total);
    } else {
      pages.push(1, '…', cur-1, cur, cur+1, '…', total);
    }
    return pages;
  }

  render();
}

/* ══════════════════════════════
   RENDU ARTICLES
══════════════════════════════ */
function renderArticle(a) {
  return `
  <div class="article-card">
    <div class="article-card-media">
      <img src="${a.img}" alt="" loading="lazy" onerror="this.style.display='none'">
    </div>
    <div class="article-card-content">
      <h3>${a.title}</h3>
      <a href="${a.url}" target="_blank" rel="noopener" class="btn">Lire l'article</a>
    </div>
  </div>`;
}

/* ══════════════════════════════
   RENDU VIDÉOS
══════════════════════════════ */
function renderVideo(v) {
  return `
  <div class="video-grid-item">
    <div class="video-card">
      <p class="video-card-title">${v.title}</p>
      <div class="video-embed-responsive">
        <iframe src="${v.embed}"
          allow="accelerometer; autoplay; clipboard-write; encrypted-media; gyroscope; picture-in-picture"
          allowfullscreen loading="lazy"></iframe>
      </div>
    </div>
  </div>`;
}

/* ══════════════════════════════
   INITIALISATION
══════════════════════════════ */
Paginator({
  data: articles,
  perPage: 6,
  gridId: 'articleGrid',
  paginationId: 'articlePagination',
  infoId: 'articlePageInfo',
  renderItem: renderArticle
});

Paginator({
  data: videos,
  perPage: 6,
  gridId: 'videoGrid',
  paginationId: 'videoPagination',
  infoId: 'videoPageInfo',
  renderItem: renderVideo
});
